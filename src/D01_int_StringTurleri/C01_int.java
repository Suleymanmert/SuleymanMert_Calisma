package D01_int_StringTurleri;

public class C01_int {
    public static void main(String[] args) {

        int sayi;
        sayi=20;
        System.out.println(sayi);// sayinin eski degeri 20

        sayi=30;
        System.out.println(sayi);// sayiya yeni deger atamasi yaptım ve yeni degerinin 30 olmasini istedim
                                 // (buradan itibaren tum sayi degerim 30 olarak yazilacaktir)

        sayi=sayi+15;
        System.out.println(sayi);// simdi ise en son sayi degerimin uzerine 15 sayisini eklemesini istedim ve yeni degerimin 45 olmsani istiyorum
                                 // (buradan itibaren tum sayi degerim 45 olarak yazilacaktir)

        sayi=sayi-30;
        System.out.println(sayi);// burada ise toplam sayim olan 45 den 30 sayisini cıkarmasini ye en som degerimin 15 olmasini istiyorum
                                 // (buradan itibaren tum sayi degerim 15 olarak yazilacaktir)

        /*
        kokdlrimiz sorunsuz bir sekilde calisiyor.

         */
    }


}
